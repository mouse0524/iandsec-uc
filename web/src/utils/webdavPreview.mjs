export function getWebdavFileExtension(fileName = '') {
  const cleanName = String(fileName).split(/[?#]/)[0]
  const dotIndex = cleanName.lastIndexOf('.')
  if (dotIndex < 0 || dotIndex === cleanName.length - 1) return ''
  return cleanName.slice(dotIndex + 1).toLowerCase()
}

export function isWebdavPreviewSupported(fileName = '') {
  return WEB_DAV_PREVIEW_EXTENSIONS.has(getWebdavFileExtension(fileName))
}
